package Enums;

/**
 * The {@code PrescriptionStatus} enum represents the status of a prescription.
 * It is used to indicate whether a prescription is pending or has been dispensed.
 */
public enum PrescriptionStatus {

    /**
     * Indicates that the prescription is pending and has not yet been dispensed.
     */
    PENDING,

    /**
     * Indicates that the prescription has been dispensed to the patient.
     */
    DISPENSED
}
