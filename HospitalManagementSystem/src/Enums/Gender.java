package Enums;

/**
 * The {@code Gender} enum represents the possible gender identities.
 * It is used to specify the gender of an individual in the system.
 */
public enum Gender {

    /**
     * Indicates that the individual identifies as male.
     */
    MALE,

    /**
     * Indicates that the individual identifies as female.
     */
    FEMALE,

    /**
     * Indicates that the individual identifies as a gender other than male or female.
     */
    OTHERS
}
