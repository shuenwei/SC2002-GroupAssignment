package src.Enums;

/**
 * The {@code RequestStatus} enum represents the status of a request.
 * It is used to indicate whether a request is pending or has been fulfilled.
 */
public enum RequestStatus {

        /**
         * Indicates that the request is pending and has not yet been fulfilled.
         */
        PENDING,

        /**
         * Indicates that the request has been fulfilled.
         */
        FULFILLED
}
